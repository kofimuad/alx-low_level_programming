#include "main.h"
/**
 * print_diagonal - prints diagonal line in the terminal
 *
 * @n: number of times to print a diagonal
 *
 * Return: Nothing.
 */
void print_diagonal(int n)
{
	int i;
	int j;

	if (n <= 0)
	{
		_putchar('\n');
	}
	else
	{
		for (i = 0; i < n; i++)
		{
			for (j = 0; j < i; j++)
			{
				_putchar(' ');
			}
			_putchar('\\');
			_putchar('\n');
		}
	}
}
